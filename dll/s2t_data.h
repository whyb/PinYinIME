// ==================== 增强版简繁转换函数 ====================
// 采用"词汇最长匹配优先, 单字回退"策略

// 简体 → 繁体
inline std::wstring toTraditional(const std::wstring& s) {
    S2T_ns::init();
    std::wstring result;
    size_t i = 0;
    while (i < s.size()) {
        bool word_matched = false;
        // 从当前位置尝试最长词汇匹配
        for (size_t len = S2T_ns::s2t_max_word_len; len >= 2; len--) {
            if (i + len > s.size()) continue;
            std::wstring sub = s.substr(i, len);
            auto it = S2T_ns::s2t_words.find(sub);
            if (it != S2T_ns::s2t_words.end()) {
                result += it->second;
                i += len;
                word_matched = true;
                break;
            }
        }
        if (word_matched) continue;

        // 单字转换
        wchar_t c = s[i];
        auto it = S2T_ns::s2t_char.find(c);
        if (it != S2T_ns::s2t_char.end()) {
            result += it->second;
        } else {
            result += c;
        }
        i++;
    }
    return result;
}

// 繁体 → 简体
inline std::wstring toSimplified(const std::wstring& s) {
    S2T_ns::init();
    std::wstring result;
    size_t i = 0;
    while (i < s.size()) {
        bool word_matched = false;
        // 从当前位置尝试最长词汇匹配 (繁体词→简体词)
        for (size_t len = S2T_ns::t2s_max_word_len; len >= 2; len--) {
            if (i + len > s.size()) continue;
            std::wstring sub = s.substr(i, len);
            auto it = S2T_ns::t2s_words.find(sub);
            if (it != S2T_ns::t2s_words.end()) {
                result += it->second;
                i += len;
                word_matched = true;
                break;
            }
        }
        if (word_matched) continue;

        // 单字转换
        wchar_t c = s[i];
        auto it = S2T_ns::t2s_char.find(c);
        if (it != S2T_ns::t2s_char.end()) {
            result += it->second;
        } else {
            result += c;
        }
        i++;
    }
    return result;
}
